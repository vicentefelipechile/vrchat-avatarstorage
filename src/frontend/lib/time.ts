export enum TimeUnit {
	Second = 1000,
	Minute = 60 * 1000,
	Hour = 60 * 60 * 1000,
	Day = 24 * 60 * 60 * 1000,
	Week = 7 * 24 * 60 * 60 * 1000,
}
